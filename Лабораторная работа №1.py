try:
    # Ввод трёх сторон треугольника
    a = int(input("Введите длину первой стороны: "))
    b = int(input("Введите длину второй стороны: "))
    c = int(input("Введите длину третьей стороны: "))
    #Проверка на отрицательные или нулевые значения
    if a <= 0 or b <= 0 or c <= 0:
        print("Ошибка: длины сторон должны быть положительными числами!")
    else:
        #Проверка условия существования треугольника
        if a + b > c and b + c > a and a + c > b:
            #Определение типа треугольника
            if a == b and b == c:
                print("Ваш треугольник равносторонний!")
            elif a == b or b == c or a == c:
                print("Ваш треугольник равнобедренный!")
            else:
                print("Ваш треугольник разносторонний!")
        else:
            print("Ошибка: треугольник с такими сторонами не существует!")
except ValueError:
    print("Ошибка: введите целые числа, а не текст!")

